    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int dia;
        
        dia = Integer.parseInt(jTextDia.getText());
         
        if (dia < 0 || dia > 7){
            JlabDiaF.setText("ERROR");
        }
        
        switch (dia){
            case 1: JlabDiaF.setText("Lunes");
            case 2: JlabDiaF.setText("Martes");
            case 3: JlabDiaF.setText("Miercoles");
            case 4: JlabDiaF.setText("Jueves");
            case 5: JlabDiaF.setText("Viernes");
            case 6: JlabDiaF.setText("Sábado");
            case 7: JlabDiaF.setText("Domingo");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

