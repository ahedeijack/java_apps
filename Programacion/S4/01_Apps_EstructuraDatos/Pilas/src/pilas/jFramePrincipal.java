    private void jButAgregaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButAgregaActionPerformed
        // TODO add your handling code here:
        Pila grupo = jRbGpo1.isSelected()? grupo1: grupo2;
        if(!grupo.Full()){
            TipoElem alu = new TipoElem(jTabAdd.getValueAt(0, 0).toString(),
                                        jTabAdd.getValueAt(0, 1).toString(),
                                        Integer.parseInt(jTabAdd.getValueAt(0, 2).toString()));
            grupo.Push(alu);
            jTabNoAlus.setValueAt(grupo.Length(), 0, jRbGpo1.isSelected()? 0 : 1);
        } else {
            JOptionPane.showMessageDialog(this, "El grupo esta lleno...");
        }
    }//GEN-LAST:event_jButAgregaActionPerformed

    private void jButVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButVerActionPerformed
        // TODO add your handling code here:
        Pila grupo = jRbGpo1.isSelected()? grupo1: grupo2;
        Pila aux = new Pila(grupo.Length());
        jLabGpo.setText(jRbGpo1.isSelected()?"Grupo 1":"Grupo 2");
        DefaultTableModel modelo = (DefaultTableModel) jTabGrupo.getModel();
        modelo.setRowCount(0);
    while(!grupo.Empty()){
        //Se crea un elemento con el pop de la pila.
       TipoElem elem = grupo.Pop();
       //En la pila auxiliar metemos el elemento al que anteriormente hicimos pop para no perderlo.
       aux.Push(elem);
       //Creamos un arreglo de objetos el cual tendra 3 parametros, los cuales seran
       //dados a partir del elemento creado por el pop de la pila grupo.
       Object[] o = new Object[3];
       o[0] = elem.getNoCon();
       o[1] = elem.getNombre();
       o[2] = elem.getCalif();
       modelo.addRow(o);
   }     
    
   while(!aux.Empty()){
       grupo.Push(aux.Pop());
   }
    }//GEN-LAST:event_jButVerActionPerformed

    private void InvertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InvertirActionPerformed
        // TODO add your handling code here:
        Pila grupoSel = jRbGpo1.isSelected()?grupo1:grupo2;
        if(grupoSel.Length()>1){
            Pila aux1 = new Pila();
            Pila aux2 = new Pila();
            while(!grupoSel.Empty())
                aux1.Push(grupoSel.Pop());
            while(!aux1.Empty())
                aux2.Push(aux1.Pop());
            while(!aux2.Empty())
                grupoSel.Push(aux2.Pop());
            JOptionPane.showMessageDialog(this, "La inversión con auxiliares ha terminado...");
                } else {
            JOptionPane.showMessageDialog(this, "La inversión no se pudo...");

        }
    }//GEN-LAST:event_InvertirActionPerformed

