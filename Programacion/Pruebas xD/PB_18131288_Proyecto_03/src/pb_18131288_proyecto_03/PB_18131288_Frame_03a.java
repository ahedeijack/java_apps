     public boolean agregarGrupoMusical(PB_18131288_Clase_A grupo)
    {
        if(bandasRegistradas == banditas.length)
            return false;
        
        banditas[bandasRegistradas] = grupo;
        bandasRegistradas++; //Incrementa el contador

        return true;
    }   
     
      public void setGrupoMusical(PB_18131288_Clase_A grupo, int posicion)
    {
        if(posicion >=0 && posicion < banditas.length)
            banditas[posicion] = grupo;
    }   
    
