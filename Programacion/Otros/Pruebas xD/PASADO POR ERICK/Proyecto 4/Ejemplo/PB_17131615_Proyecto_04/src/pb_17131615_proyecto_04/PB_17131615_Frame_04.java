    public void comenzar()
    {
        Random azar = new Random();
        
        int j=0;
        int x = 30;
        int y = 50;
        for (int i = 0; i <dato.length; i++) 
        {
            
            if(i<6)
            dato[i] = signos[azar.nextInt(signos.length)];
            else
                dato[i] = dato[j++];
        }
        for (int i = 0; i <12; i++) 
        {  
            
            pares[i]= new Boton(dato[i],"Arial",tama,2);
            pares[i].setLocation(x, y);
            
            x+=100;
            
            if ((i+1) % 4 == 0)
            {
                y+=100;
                x= 30;
            }
            
            pares[i].addActionListener(this);
            this.add(pares[i]);  
        }
        
        parEncontrados = 0;
        intentos = 0;
        
        jLabPares.setText(parEncontrados+"");
        jLabIntentos.setText(intentos+"");
    }
