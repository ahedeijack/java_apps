    private Animales [] getGrupoSel() {
        int grupoSelec = jCbGrupo.getSelectedIndex();
        switch (grupoSelec) {
            case 0: 
                return grupo1;
            case 1: 
                return grupo2;
            default:
                return grupo1;
        }
}
