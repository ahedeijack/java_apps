    private void jButCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCalcularActionPerformed
        // TODO add your handling code here
       double [][] ventas = new double [modelo.getRowCount()][modelo.getColumnCount()];
       DefaultListModel lista = new DefaultListModel();  
       
       double ventasTotales = 0;
        for(int r = 0; r<modelo.getRowCount(); r++){
            for(int c = 0; c<modelo.getColumnCount(); c++){
                
                ventasTotales = (double) modelo.getValueAt(r, c);
                jLabVen.setText("$"+ventasTotales);
                ventas[r][c] = Double.parseDouble(modelo.getValueAt(r, c)+"");
                lista.addElement(ventas[r][c]);
                
                //ordenamiento
                
        }   
        }
        jLisVen.setModel(lista);
        
        
       double prom;
       prom = promedio(ventasTotales, jTabVen);
       jLabProm.setText("$"+prom);
       
       
       
          
        
    }//GEN-LAST:event_jButCalcularActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        double [] [] ventas = new double [modelo.getRowCount()][modelo.getColumnCount()];
        double orden = 0;
      for(int r = 0; r<ventas.length-1; r++){
      for(int c = 0; c<ventas.length; c++){
      for(int x = 0; x<r; x++){
      for(int y = 0; y<c; y++){
                    
                        if(ventas[r][c]>ventas[x][y]){
                           orden = ventas[r][c];
                           ventas[r][c] = ventas[x][y];
                           ventas[x][y] = orden;
                        }
                        
       }
      }      
     }
    }  
        
   for(int r = ventas.length-1; r>=0; r--){
            for(int c = ventas.length-1; c>=0; c--){
                modelo.setValueAt(ventas[r][c], r, c);
        }   
        }
      
    }//GEN-LAST:event_jButton2ActionPerformed

