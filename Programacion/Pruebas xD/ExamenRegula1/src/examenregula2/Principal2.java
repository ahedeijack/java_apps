    public void mostrar(){
        double suma = 0;
        for(int i = 0; i<ex.length; i++){
            modelo.setValueAt(ex[i].getMasa(), i, 0);
            modelo.setValueAt(ex[i].getAceleracion(), i, 1);
            modelo.setValueAt(ex[i].fuerza(), i, 2);
            
            suma=((double) modelo.getValueAt(i, 2)+suma);
        }
        
        jLabTotal.setText(suma+"");
    }

