    // métodos del frame
    public void mostrar()
    {
        double totalPerimetro=0;
        double totalAltura=0;
        
        for (int r=0; r<lista.length; r++)
        {
            jTableRectangulo.setValueAt(lista[r].getNombre(), r, 0);
            jTableRectangulo.setValueAt(lista[r].getBase(), r, 1);
            jTableRectangulo.setValueAt(lista[r].altura(), r, 2);
            jTableRectangulo.setValueAt(lista[r].getArea(), r, 3);
            jTableRectangulo.setValueAt(lista[r].perimetro(), r, 4);
            
            totalPerimetro+=lista[r].perimetro();
            totalAltura+=lista[r].altura();
        }
        jLabPT.setText(totalPerimetro+"");
        jLabPA.setText(totalAltura/3+"");
    }

