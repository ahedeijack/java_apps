    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Object o[] = new Object[3];
         nombre = jTextNombre.getText();
         energia = Double.parseDouble(jTextEnergia.getText()+"");
         velocidad = Double.parseDouble(jTextVelocidad.getText()+"");
                        o[0] = nombre;
                        o[1] = energia;
                        o[2] = velocidad;
                        
         if (nombre.compareTo("")<0 || energia == 0 || velocidad == 0) {
            JOptionPane.showMessageDialog(this, "necesitas llenar todos los parametros");
        } else {
              int renglon = Integer.parseInt(jSpinPos.getValue()+"");        
                if (renglon == modelo.getRowCount()) {
                        modelo.addRow(o);
             } else {
                    modelo.setValueAt(o[0], renglon, 0);
                    modelo.setValueAt(o[1], renglon, 1);
                    modelo.setValueAt(o[2], renglon, 2);
                }
         
        }
         
    jTextNombre.setText("null");
    jTextEnergia.setText("0");
    jTextVelocidad.setText("0");
    jSpinPos.setValue(Integer.parseInt(modelo.getRowCount()+""));
    jSpin1.setValue(Integer.parseInt(modelo.getRowCount()+""));
         

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCalcularActionPerformed
        // TODO add your handling code here:
        llenar();
        
                Energia nueva = new Energia(0,0);
                Cuerpo mayor = new Cuerpo("",nueva);
                Cuerpo menor = cuerpos[0];
                
                for (int i = 0; i < cuerpos.length; i++) {
                    if (cuerpos[i].getEnergia().obtenerMasa() > mayor.getEnergia().obtenerMasa()) {
                        mayor = cuerpos[i];        
                    }
                    
                    if (cuerpos[i].getEnergia().obtenerMasa() < menor.getEnergia().obtenerMasa()) {
                        menor = cuerpos[i];
                    }
            }
                
                jLabMayorNombre.setText(mayor.getNombre());
                jLabMayorMasa.setText(mayor.getEnergia().obtenerMasa()+"");
                
                 jLabMenorNombre.setText(menor.getNombre());
                jLabMenorMasa.setText(menor.getEnergia().obtenerMasa()+"");
    }//GEN-LAST:event_jButCalcularActionPerformed

    private void jButOrdenMasaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButOrdenMasaActionPerformed
        // TODO add your handling code here:
                jButCalcular.doClick();
                //Cuerpos 
                for (int i = 0; i < cuerpos.length-1; i++) {
                    for (int j = 0; j < cuerpos.length; j++) {
                        if (cuerpos[i].getEnergia().obtenerMasa() < cuerpos[j].getEnergia().obtenerMasa()) {
                            Cuerpo orden = cuerpos[i];
                            cuerpos[i] = cuerpos[j];
                            cuerpos[j] = orden;
                        }
                    }
        }
                
                modelo.setRowCount(0);
                modelo.setRowCount(cuerpos.length);
                for (int i = 0; i < cuerpos.length; i++) {
                   modelo.setValueAt(cuerpos[i].getNombre(), i, 0);
                   modelo.setValueAt(cuerpos[i].getEnergia().getEnergia(), i, 1);
                   modelo.setValueAt(cuerpos[i].getEnergia().getVelocidad(), i, 2);
                   modelo.setValueAt(cuerpos[i].getEnergia().obtenerMasa(), i, 3);
                }
        
    }//GEN-LAST:event_jButOrdenMasaActionPerformed

