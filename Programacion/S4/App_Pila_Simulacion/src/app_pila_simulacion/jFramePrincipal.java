    public void mostrar(Pila mostrar, DefaultTableModel model){
        //mientras la pila sea diferente al estado vacio, realizar el codigo.
        model.setRowCount(0);
        
        while(!mostrar.Empty()){
            Elemento dato = mostrar.Pop();
            auxis.Push(dato);
            Object o [] = new Object[3];
            
            o[0] = dato.getEspecialidad();
            switch (dato.getTamaño()) {
                case 0:
                    o[1] = "Chica";
                    break;
                case 1:
                    o[1] = "Mediana";
                    break;
                case 2:
                    o[1] = "Grande";
                    break;
                default:
                    break;
            }
            o[2] = dato.getPrecio();
            model.addRow(o);
        }
        while(!auxis.Empty()){
            mostrar.Push(auxis.Pop());
        }
        
        
    }
    
    public void cambios(Pila inicio, Pila fin){
        Elemento dato;
        if(inicio.Empty()){
            JOptionPane.showMessageDialog(this, "La pila inicial esta vacía.");
        } else if(fin.Full()){
            JOptionPane.showMessageDialog(this, "La pila final esta llena.");
        } else {
            //Sacaremos el ultimo elemento de la pila de inicio  
            dato = inicio.Pop();
            fin.Push(dato);
        }
    }
    
