            Sdep.setLibra(Integer.parseInt(jTable1.getValueAt(r, 7) + ""));
            Sdep.setChelin(Integer.parseInt(jTable1.getValueAt(r, 8) + ""));
            Sdep.setPenique(Integer.parseInt(jTable1.getValueAt(r, 9) + ""));

            salTot = Sfinal.resta(Sinicial);

            totini = totini.Suma(Sinicial);
