    private void jMenuItemGTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemGTActionPerformed
        // TODO add your handling code here: 
        jfch.showSaveDialog(jMenu1);
    
        File archivo = jfch.getSelectedFile();
        
        if(archivo == null)
            return;
        
        try {
            // TODO add your handling code here:
            FileWriter fw = new FileWriter(archivo);
            PrintWriter pw = new PrintWriter(fw);
                        
            pw.println((int)jSpinAutos.getValue());
            
            for (int r=0; r<(int)jSpinAutos.getValue(); r++)
            {
                pw.println(jTableAutos.getValueAt(r, 0)+"");
                pw.println(jTableAutos.getValueAt(r, 1)+"");
                pw.println(jTableAutos.getValueAt(r, 2)+"");
            }
       
            pw.close();
            fw.close();
            
            JOptionPane.showMessageDialog(this, " Archivo Grabado");
            
        } catch (IOException ex) {
            Logger.getLogger(PB_17130852_Frame_01.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jMenuItemGTActionPerformed

    private void jMenuItemLTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemLTActionPerformed
        // TODO add your handling code here:
        jfch.showOpenDialog(jMenu1);
    
        File archivo = jfch.getSelectedFile();
        
        if(archivo == null)
            return;
        
        try {
            // TODO add your handling code here:
            FileReader fr = new FileReader(archivo);
            BufferedReader br = new BufferedReader(fr);
            
            
            try {
                int n = Integer.parseInt(br.readLine());

            jSpinAutos.setValue(n);
            
            for (int r=0; r<(int)jSpinAutos.getValue(); r++)
            {
                jTableAutos.setValueAt(br.readLine(), r, 0);
                jTableAutos.setValueAt(br.readLine(), r, 1);
                jTableAutos.setValueAt(br.readLine(), r, 2);
            }
            
            br.close();
            fr.close();
                
            } catch (IOException ex) {
                Logger.getLogger(PB_17130852_Frame_01.class.getName()).log(Level.SEVERE, null, ex);
            }

 
        } catch (FileNotFoundException ex) {
            Logger.getLogger(PB_17130852_Frame_01.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jMenuItemLTActionPerformed

