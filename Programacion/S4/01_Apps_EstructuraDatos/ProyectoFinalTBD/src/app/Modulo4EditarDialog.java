    private boolean validarDatos() {
        int id;
        try {
            id = Integer.parseInt(jTextIDCliente.toString());
        } catch (NumberFormatException ex) {
            dialogoMensaje("Debe capturar un valor num√©rico v√°lido");
            jTextIDCliente.requestFocus();
            return false;
        }
        
        String nombre = jTextNombre.getText();
        if (nombre.trim().equals("")) {
            dialogoMensaje("No se permite un valor en blanco");
            jTextNombre.requestFocus();
            return false;
        }
        
        String apPat = jTextApellidoPat.getText();
        if (apPat.trim().equals("")) {
            dialogoMensaje("No se permite un valor en blanco");
            jTextApellidoPat.requestFocus();
            return false;
        }
        
        String apMat = jTextApellidoMat.getText();
        if (apMat.trim().equals("")) {
            dialogoMensaje("No se permite un valor en blanco");
            jTextApellidoMat.requestFocus();
            return false;
        }
        
        String telefono = jTextTelefono.getText();
        if (telefono.trim().equals("")) {
            dialogoMensaje("No se permite un valor en blanco");
            jTextTelefono.requestFocus();
            return false;
        }
        
        String correo = jTextCorreo.getText();
        if (correo.trim().equals("")) {
            dialogoMensaje("No se permite un valor en blanco");
            jTextCorreo.requestFocus();
            return false;
        }
        
        int idSuc;
        try {
            idSuc = Integer.parseInt(jTextIDCliente.toString());
        } catch (NumberFormatException ex) {
            dialogoMensaje("Debe capturar un valor num√©rico v√°lido");
            jTextIDSucursal.requestFocus();
            return false;
        }
        
        
        return true;
    }
    
