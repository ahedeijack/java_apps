    private void ContarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ContarActionPerformed
        for (int r=0; r<(int)jSpinner1.getValue(); r++)
        {
            if("God Of War PS4"==jTable1.getValueAt(r, 0))
                ++a;
            if("Halo 5"==jTable1.getValueAt(r, 0))
                ++b;
            if("TLOZ: Breath Of The Wild"==jTable1.getValueAt(r, 0))
                ++c;
            if("Dragon Ball Fighter Z"==jTable1.getValueAt(r, 0))
                ++d;
        }
        god.setText(""+a);
        hal.setText(""+b);
        zelda.setText(""+c);
        z.setText(""+d);
    }//GEN-LAST:event_ContarActionPerformed

