    private void jListEmpleadosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListEmpleadosValueChanged
        // TODO add your handling code here:
        String nombre = jListEmpleados.getSelectedValue();
        
        for(int  i = 0; i<jfMenu.getEmpleadosRegistrados(); i++){
            Empleado x = jfMenu.getEmpleado(i);
            
            if(nombre.equals(x.getNombre())){
                jTextArea.setText(x.toString()+"\n\nSueldo $"+x.sueldo());
            }
        }
        
        
    }//GEN-LAST:event_jListEmpleadosValueChanged

