    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String xx1 = this.jTextField1.getText();
        int x1 = Integer.parseInt(xx1);
        
        String yy1 = this.jTextField2.getText();
        int y1 = Integer.parseInt(yy1);
        
        int x2 = Integer.parseInt(this.jTextField4.getText());
        int y2 = Integer.parseInt(this.jTextField34.getText());
        
        Punto p1 = new Punto(x1,y1);
        Punto p2 = new Punto(x2,y2);
        double dist;
        
        dist = p1.Distancia(p2);
        
        DecimalFormat formato = new DecimalFormat("#.##"); //Dos digitos.
        this.jLabRes.setText(jLabRes.getText()+" "+formato.format(dist));
                                        //Dibujar en la forma
                                        
                                        
         Graphics g = this.jPanel3.getGraphics();
         
         g.setColor(Color.black);
         g.drawString("x1,y1", p1.getX(), p1.getY());
         g.drawString("x2, y2", p2.getX(), p2.getY());
         g.drawLine(p1.getX(), p1.getY(), p2.getX(), p2.getY());
         
    }//GEN-LAST:event_jButton1ActionPerformed

