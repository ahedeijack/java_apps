    public void actionPerformed(ActionEvent ae) {
        pregunta++;
        Boton botonc = (Boton)ae.getSource(); 
        botonc.mostrarSigno();
        if((pregunta%2) != 0)
        {
            
            botond.setSigno(botonc.getSigno());
            posX =botonc.getX();
            posY =botonc.getY();
            botonc.setEnabled(false); 
            
            return;
        }
        
         if((pregunta%2)==0)
         {
            botonc.mostrarSigno();
            intentos++;
            jLabIntentos.setText(intentos+"");
        
        if (botond.getSigno().equals(botonc.getSigno()))
        {
            botonc.setEnabled(false);
            parEncontrados++;
            jLabPares.setText(parEncontrados+"");
        }  
        else    
            {
             pares[posicion(posX,posY)].setEnabled(true);
             pares[posicion(posX,posY)].setText("");
             botonc.setText("");
            }
         }
    }

    

