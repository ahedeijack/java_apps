    private void jButLlenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButLlenarActionPerformed
        // TODO add your handling code here:
        Random r = new Random();
        car = new Coche[modelo.getRowCount()];
        int prec = 100000;
        
        for(int i = 0;  i<modelo.getRowCount(); i++){
           
            
            modelo.setValueAt("Carro "+i, i, 0);
            modelo.setValueAt((prec+r.nextInt(10000)), i, 1);
            modelo.setValueAt((prec+r.nextInt(10000)+10000), i, 2);
        }
        
        for(int i = 0; i<car.length; i++){
            
            car[i]= new Coche(""+modelo.getValueAt(i, 0),
                              Double.parseDouble(""+modelo.getValueAt(i, 1)),
                              Double.parseDouble(""+modelo.getValueAt(i, 2)),
                              0.0);
        }
        
        for(int i = 0; i<car.length; i++){
            modelo.setValueAt(car[i].margen(), i, 3);
        }
        
        mostrar();
        
    }//GEN-LAST:event_jButLlenarActionPerformed

    private void jButOrdenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButOrdenarActionPerformed
        // TODO add your handling code here:
        car = new Coche[modelo.getRowCount()];
        
        for(int i = 0; i<car.length; i++){
            
            car[i]= new Coche(""+modelo.getValueAt(i, 0),
                              Double.parseDouble(""+modelo.getValueAt(i, 1)),
                              Double.parseDouble(""+modelo.getValueAt(i, 2)),
                              Double.parseDouble(""+modelo.getValueAt(i, 3)));
            }
        
        
        for(int i = 0; i<modelo.getRowCount()-1; i++){
            for(int j = 0; j<modelo.getRowCount(); j++){
                
                if(car[i].margen()>car[j].margen()){
                    Coche aux = car[i];
                    car[i]=car[j];
                    car[j]=aux;
                }
            }
        }
        mostrar();
    }//GEN-LAST:event_jButOrdenarActionPerformed

