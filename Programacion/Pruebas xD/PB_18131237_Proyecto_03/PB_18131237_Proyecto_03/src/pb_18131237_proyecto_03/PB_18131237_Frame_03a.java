    public PB_18131237_Frame_03a(PB_18131237_Frame_03 jfm) {
        initComponents();
        
        jfMenu = jfm;
        
        copiaDatos = new GrupoMusical[jfMenu.getGruposRegistrados()];
        dibujos = new IMostrar[copiaDatos.length];
        
        for(int i=0; i<copiaDatos.length; i++)
        {
            copiaDatos[i] = jfMenu.getGrupoMusical(i);
            dibujos[i] = jfMenu.getDibujos(i);
        }    
        
        zoe.setEstado("Cuernavaca, MX");
        banda.setEstado("Sinaloa, MX");
        foo.setPais("Seattle, EEUU");
        nir.setPais("Aberdeen, EEUU");
        
        zoe.setNumAlbumes(5);
        banda.setNumAlbumes(4);
        foo.setNumAlbumes(9);
        nir.setNumAlbumes(3);
        
        lugar[0] = zoe.getEstado();
        lugar[1] = banda.getEstado();
        lugar[2] = foo.getPais();
        lugar[3] = nir.getPais();
        
        numAlbum[0] = zoe.getNumAlbumes();
        numAlbum[1] = banda.getNumAlbumes();
        numAlbum[2] = foo.getNumAlbumes();
        numAlbum[3] = nir.getNumAlbumes();
        
        for(int i=0; i<copiaDatos.length; i++)
        {    
            for(int j = i+1; j<copiaDatos.length; j++)
                if(copiaDatos[i].getGenero().compareTo(copiaDatos[j].getGenero()) > 0)
                {
                    GrupoMusical aux = copiaDatos[i];
                    copiaDatos[i] = copiaDatos[j];
                    copiaDatos[j] =  aux;
                    
                    String auxs = lugar[i];
                    lugar[i] = lugar[j];
                    lugar[j] = auxs;
                    
                    int auxi = numAlbum[i];
                    numAlbum[i] = numAlbum[j];
                    numAlbum[j] = auxi;
                    
                    IMostrar auxd = dibujos[i];
                    dibujos[i] =dibujos[j];
                    dibujos[j] = auxd;
                }
        }
        mostrar();
    }
    
