    private void jButt_AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButt_AgregarActionPerformed
        // TODO add your handling code here:
        int p = cola.primero();
        int u = cola.ultimo();
        
        u++;
        
        int n = Integer.parseInt(String.valueOf(jCB_Opc.getSelectedIndex()));
        if((p<modeloMatriz.getColumnCount()&&p<u)||u<p){
           if(n==0){
                int m = Integer.parseInt(String.valueOf(jTF_Agregar.getText()));
                modeloMatriz.setValueAt(m, 0, u);
                if(p==-1)
                    p++;
                if(u==modeloMatriz.getColumnCount()-1)
                    u=0;
                if(p==modeloMatriz.getColumnCount()-1)
                    p=0;
            }
            if(n==1){
                double m = Double.parseDouble(String.valueOf(jTF_Agregar.getText()));
                modeloMatriz.setValueAt(m, 0, u);
                if(p==-1)
                    p++;
                if(u==modeloMatriz.getColumnCount()-1)
                    u=0;
                if(p==modeloMatriz.getColumnCount()-1)
                    p=0;
            }
            if(n==0){
                String m = jTF_Agregar.getText();
                modeloMatriz.setValueAt(m, 0, u);
                if(p==-1)
                    p++;
                if(u==modeloMatriz.getColumnCount()-1)
                    u=0;
                if(p==modeloMatriz.getColumnCount()-1)
                    p=0;
            } 
        }
        else
            JOptionPane.showMessageDialog(null, "Overflow!!");
        jTF_Agregar.setText("");

    }//GEN-LAST:event_jButt_AgregarActionPerformed

    private void jButt_BorarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButt_BorarActionPerformed
        // TODO add your handling code here:
        int p = cola.primero();
        int u = cola.ultimo();
        
        int n = Integer.parseInt(String.valueOf(jCB_Opc.getSelectedIndex()));
        if((p<modeloMatriz.getColumnCount()&&p<u)||u<p){
            if(n==0){
                int m = Integer.parseInt(String.valueOf(modeloMatriz.getValueAt(0, p)));
                modeloMatriz.setValueAt("", 0, p);
                p++;
            }
            if(n==1){
                Double m = Double.parseDouble(String.valueOf(modeloMatriz.getValueAt(0, p)));
                modeloMatriz.setValueAt("", 0, p);
                p++;
            }
            if(n==2){
                String m = String.valueOf(modeloMatriz.getValueAt(0, p));
                modeloMatriz.setValueAt("", 0, p);
                p++;
            }
        }
        else
            JOptionPane.showMessageDialog(null,"UNDERFLOW");
    }//GEN-LAST:event_jButt_BorarActionPerformed

