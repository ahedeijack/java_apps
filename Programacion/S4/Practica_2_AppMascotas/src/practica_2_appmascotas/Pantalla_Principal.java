    private void jButtonVisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVisualizarActionPerformed
        Mascota [] grupoSel = getGrupoSel();
        int nGpoSelec = jComboGrupo.getSelectedIndex();
        this.jLabelTituloGrupo_.setText("GRUPO " + (this.jComboGrupo.getSelectedIndex()==0?'A':'B'));
        DefaultTableModel modelo = (DefaultTableModel) jTableGrupo.getModel();        
        while (jTableGrupo.getRowCount() > 0)
            modelo.removeRow(0);
        for(int i =0; i < nMasxGpo[nGpoSelec]; i++) {
            Object[] o = new Object[7];
            o[0] = i+1;
            o[1] = grupoSel[i].getNombre();
            o[2] = grupoSel[i].getTipo();
            o[3] = grupoSel[i].getEdad();
            o[4] = grupoSel[i].getNumVacunas();
            o[5] = grupoSel[i].getCostVacunas();
            o[6] = grupoSel[i].getSexo();
            modelo.addRow(o);
        }
    }//GEN-LAST:event_jButtonVisualizarActionPerformed

