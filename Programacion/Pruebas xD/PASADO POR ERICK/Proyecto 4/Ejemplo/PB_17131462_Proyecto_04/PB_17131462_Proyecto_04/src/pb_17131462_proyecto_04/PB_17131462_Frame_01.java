    private void GraficaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GraficaActionPerformed
       JFreeChart Grafica;
        DefaultCategoryDataset Datos = new DefaultCategoryDataset();

        Datos.addValue((int)a, "Votos", "God of War PS4");
        Datos.addValue((int)b, "Votos", "Halo 5");
        Datos.addValue((int)c, "Votos", "TLOZ: Breath Of The Wild");
        Datos.addValue((int)d, "Votos", "Dragon Ball Fighter Z");

        Grafica = ChartFactory.createBarChart("Votaciones",
        "Nominados", "Resultados", Datos,
        PlotOrientation.VERTICAL, true, true, false);

        ChartPanel Panel = new ChartPanel(Grafica);
        JFrame Ventana = new JFrame("Game Awards");
        Ventana.getContentPane().add(Panel);
        Ventana.pack();
        Ventana.setVisible(true);
        Ventana.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
    }//GEN-LAST:event_GraficaActionPerformed

