    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String [][] cir = new String[modelo.getRowCount()][modelo.getColumnCount()];

        for (int i = 0; i < modelo.getRowCount() - 1; i++)
        {
            cir[i][0] = modelo.getValueAt(i, 0) + "";
            
            for (int j = i + 1; j < modelo.getRowCount(); j++)
            {
                cir[j][0] = modelo.getValueAt(j, 0) + "";

                if (cir[i][0].compareTo(cir[j][0]) > 0)
                {
                    for (int c = 0; c < modelo.getColumnCount(); c++)
                    {
                        cir[i][c] = modelo.getValueAt(j, c) + "";
                        
                        modelo.setValueAt(modelo.getValueAt(i, c), j, c);
                        modelo.setValueAt(cir[i][c], i, c);
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        FMM _fmm [] = new FMM[modelo.getRowCount()];
        
        ModulosMod sist = new ModulosMod();
        Object fmm;
        
        for(int r = 0; r<_fmm.length;r++){
            sist.setIms(Integer.parseInt(modelo.getValueAt(r,1)+""));
            sist.setMt(Integer.parseInt(modelo.getValueAt(r,2)+""));
            sist.setFa(Integer.parseInt(modelo.getValueAt(r,3)+""));
            sist.setFe(Integer.parseInt(modelo.getValueAt(r,4)+""));
            
            fmm = modelo.getValueAt(r, 5);
            
            _fmm [r]= new FMM(modelo.getValueAt(r, 0)+"",sist,fmm);
        }
          
        for(int r = 0; r<modelo.getRowCount();r++){
            modelo.setValueAt(_fmm[r].getNombre(), r, 0);
            modelo.setValueAt(_fmm[r].getSistema().getIms(), r, 1);
            modelo.setValueAt(_fmm[r].getSistema().getMt(), r, 2);
            modelo.setValueAt(_fmm[r].getSistema().getFa(), r, 3);
            modelo.setValueAt(_fmm[r].getSistema().getFe(), r, 4);
            
            modelo.setValueAt(_fmm[r].getSistema().Metodo(), r, 5);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

