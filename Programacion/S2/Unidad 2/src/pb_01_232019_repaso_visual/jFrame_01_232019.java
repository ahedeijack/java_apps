        public static double FunVol(double r, double h){
        double v;
        v = Math.PI * Math.pow(r, 2) * h;
        return v;
                }
     public static double FunArea(double r, double h){
        double a;
        a = 2 * Math.PI * r * (r + h);
        return a;
     }
