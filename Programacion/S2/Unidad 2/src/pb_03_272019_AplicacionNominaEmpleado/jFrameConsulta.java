    public jFrameConsulta(jFrameMenu jfm) {
        initComponents();
        
        jfMenu = jfm;
        
        jLabEmpReg1.setText(jfMenu.getEmpleadosRegistrados()+"");
        
        jListEmpleados.getModel(); //vamos a empezar a construir el modelo (hacerlo desde cero)
        
        String [] nom = new String[jfMenu.getEmpleadosRegistrados()];
        
        for(int i=0; i<jfMenu.getEmpleadosRegistrados(); i++)
            nom[i] = jfMenu.getEmpleado(i).getNombre(); //guardamos loos nombres de los empleados en un arreglo de String
        
        Arrays.sort(nom);
        
        
        modeloLista = new DefaultListModel();
        
        for(int i=0; i<jfMenu.getEmpleadosRegistrados(); i++)
            modeloLista.addElement(nom[i]); 
        
        jListEmpleados.setModel(modeloLista);
        
    }

