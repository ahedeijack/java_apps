    public void mostrar()
    {
        double total = 0;
        
        
        
        for(int r=0; r<copia.length; r++)
        {
            
            for(int c=0; c<modelo.getColumnCount(); c++)
            {    
                modelo.setValueAt(null,r,c);
            }
            
            modelo.setValueAt(copia[r].getNumero(), r, 0);
            modelo.setValueAt(copia[r].getNombre(), r, 1);
            
            if(copia[r] instanceof Taller) //Un objeto es una instancia de una clase
            {
                Taller tal = (Taller)copia[r]; //el mismo objeto o hacemos de referencia de Taller
                
                modelo.setValueAt(tal.getPiezas(),r,2);
                modelo.setValueAt(tal.getPrecioUnitario(), r, 3);
            }    
            
            if(copia[r] instanceof Ventas) //Un objeto es una instancia de una clase
            {
                Ventas ven = (Ventas)copia[r]; //el mismo objeto o hacemos de referencia de Taller //un casteo
                
                modelo.setValueAt(ven.getVentasTotales(),r,4);
                modelo.setValueAt(ven.getComision(), r, 5);
            }  
            
            if(copia[r] instanceof Administrativo) //Un objeto es una instancia de una clase
            {
                Administrativo adm = (Administrativo)copia[r]; //el mismo objeto o hacemos de referencia de Taller //un casteo
                
                modelo.setValueAt(adm.getDiasTrabajados(),r,6);
                modelo.setValueAt(adm.getSueldoDiario(), r, 7);
            }    
            
            if(copia[r] instanceof Ejecutivo) //Un objeto es una instancia de una clase
            {
                Ejecutivo eje = (Ejecutivo)copia[r]; //el mismo objeto o hacemos de referencia de Taller //un casteo
                
                modelo.setValueAt(eje.getDiasTrabajados(),r,6);
                modelo.setValueAt(eje.getSueldoDiario(), r, 7);
                modelo.setValueAt(eje.getBono(), r, 8);
            }   
            
            total += copia[r].sueldo();
            
            modelo.setValueAt(copia[r].sueldo(), r, 9);
        }
        
        jLabSueldoTotal.setText(total+"");
       
    }        

