    public void Mostrar()
    {
        double Total=0;
        double TotalAltura=0;
        double PromedioAltura=0;
        
        for (int R=0; R<lista.length; R++)
        {
            jTableRectangulo.setValueAt(lista[R].getNombre(), R, 0);
            jTableRectangulo.setValueAt(lista[R].getBase(), R, 1);
            jTableRectangulo.setValueAt(lista[R].Altura(), R, 2);
            jTableRectangulo.setValueAt(lista[R].AreaTotal(), R, 3);
            jTableRectangulo.setValueAt(lista[R].getPerimetro(), R, 4);               
               
            
            Total+=lista[R].AreaTotal();
            TotalAltura+=lista[R].Altura();
            PromedioAltura= TotalAltura/lista.length;
        }
        jLabTotal.setText(Total+"");
        jLabPromAltura.setText(PromedioAltura+"");
    }
    

