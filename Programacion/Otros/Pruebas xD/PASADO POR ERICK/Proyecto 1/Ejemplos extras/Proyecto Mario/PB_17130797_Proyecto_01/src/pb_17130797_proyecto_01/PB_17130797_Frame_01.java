    private void jButAlfaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButAlfaActionPerformed
       
         for (int i=0; i<lista.length-1; i++)
            for (int j=i+1; j<lista.length; j++)
                if (lista[i].getNombre().compareTo(lista[j].getNombre())>0)
                {
                    Rectangulo aux = lista[i];
                    lista[i] = lista[j];
                    lista[j] = aux;
                }
        
        Mostrar();
        
    }//GEN-LAST:event_jButAlfaActionPerformed

    private void jButMayorMenorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButMayorMenorActionPerformed
       
         Rectangulo Mayor = lista[0];

        for (int i=1; i<lista.length; i++)
            if (lista[i].AreaTotal()>Mayor.AreaTotal())
                Mayor = lista[i];
        
        Rectangulo Menor = lista[0];

        for (int i=1; i<lista.length; i++)
            if (lista[i].AreaTotal()<Menor.AreaTotal())
                Menor = lista[i];
        
        jLabMayor.setText(Mayor.getNombre());
        jLabMenor.setText(Menor.getNombre());
        
    }//GEN-LAST:event_jButMayorMenorActionPerformed

    private void jButAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButAreaActionPerformed
        
         for (int i=0; i<lista.length-1; i++)
            for (int j=i+1; j<lista.length; j++)
                if (lista[i].AreaTotal()<lista[j].AreaTotal())
                {
                    Rectangulo aux = lista[i];
                    lista[i] = lista[j];
                    lista[j] = aux;
                }
        
        Mostrar();
        
    }//GEN-LAST:event_jButAreaActionPerformed

