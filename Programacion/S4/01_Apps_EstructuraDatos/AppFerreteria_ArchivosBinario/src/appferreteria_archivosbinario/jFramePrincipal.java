    private void jEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEliminarActionPerformed
        // TODO add your handling code here:
        String clave = JOptionPane.showInputDialog("Ingresa el ID del elemento para eliminar");
        if (clave.equals("") || Integer.parseInt(clave+"")<1000) {
                        JOptionPane.showMessageDialog(null, "El ID no ha sido localizado.");
        } else {
                     int eliminar;
                     for (int i = 0; i < modelo.getRowCount(); i++) {
                            eliminar = Integer.parseInt(modelo.getValueAt(i, 0)+"");
                            if (eliminar == Integer.parseInt(clave)) {
                                modelo.removeRow(i);
                                JOptionPane.showMessageDialog(null, "El elemento "+clave+" se elimino satisfactoriamente. Estaba en la fila "+(i+1));
                            }
                    } 
        }

    }//GEN-LAST:event_jEliminarActionPerformed

    private void jOrdenIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jOrdenIdActionPerformed
        // TODO add your handling code here:
          if (modelo.getRowCount() == 0) {
         JOptionPane.showMessageDialog(this, "Necesitas agregar datos al inventario.");
                } else{
          Producto prod[] = new Producto[modelo.getRowCount()];
          for (int i = 0; i < modelo.getRowCount(); i++) {      
          prod[i] = new Producto(Integer.parseInt(modelo.getValueAt(i, 0)+""),
                                                    (String)modelo.getValueAt(i, 1),
                                                    (String)modelo.getValueAt(i, 2),
                                                    Integer.parseInt(modelo.getValueAt(i, 3)+""),
                                                    Double.parseDouble(modelo.getValueAt(i, 4)+""));
          }
      
         arbol = new ArbolBinarioDeBusqueda();
         
         for (int i = 0; i < prod.length; i++) {
                 arbol.insertar(prod[i].getId());
         }
             
        ArchivosBinarios.ordQS_A_ID(prod, 0, prod.length-1);

        for (int i = 0; i < prod.length; i++) {
            modelo.setValueAt(prod[i].getId(), i, 0);
            modelo.setValueAt(prod[i].getTipo(), i, 1);
            modelo.setValueAt(prod[i].getMarca(), i, 2);
            modelo.setValueAt(prod[i].getCantidad(), i, 3);
            modelo.setValueAt(prod[i].getPrecio(), i, 4);
        }
        }
    }//GEN-LAST:event_jOrdenIdActionPerformed

