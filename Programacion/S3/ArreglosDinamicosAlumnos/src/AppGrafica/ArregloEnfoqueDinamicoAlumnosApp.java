    private int getNoDamasEnGrupo(ArrayList<Alumno> grupo){
        int noDamas = 0;
        for(Alumno alu:grupo)
            if(alu.getSexo()=='F'){
                noDamas++;
            }
        
        return noDamas;
    }
    
