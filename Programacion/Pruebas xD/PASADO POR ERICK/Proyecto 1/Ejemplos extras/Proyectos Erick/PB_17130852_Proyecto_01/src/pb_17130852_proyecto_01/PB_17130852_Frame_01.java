    private void jButDescendenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButDescendenteActionPerformed
        // TODO add your handling code here:
        for (int i=0; i<lista.length-1; i++)
            for (int j=i+1; j<lista.length; j++)
                if (lista[i].altura()<lista[j].altura())
                {
                    Rectangulo aux = lista[i];
                    lista[i] = lista[j];
                    lista[j] = aux;
                }
        
        mostrar();
    }//GEN-LAST:event_jButDescendenteActionPerformed

    private void jButAlfabeticoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButAlfabeticoActionPerformed
        // TODO add your handling code here:
        for (int i=0; i<lista.length-1; i++)
            for (int j=i+1; j<lista.length; j++)
                if (lista[i].getNombre().compareTo(lista[j].getNombre())>0)
                {
                    Rectangulo aux = lista[i];
                    lista[i] = lista[j];
                    lista[j] = aux;
                }
        
        mostrar();
    }//GEN-LAST:event_jButAlfabeticoActionPerformed

    private void jButMaMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButMaMeActionPerformed
        // TODO add your handling code here:
        Rectangulo may = lista[0];

        for (int i=1; i<lista.length; i++)
            if (lista[i].altura()>may.altura())
                may = lista[i];
        
        Rectangulo men = lista[0];

        for (int i=1; i<lista.length; i++)
            if (lista[i].altura()<men.altura())
                men = lista[i];
        
        jLabMayor.setText(may.getNombre());
        jLabMenor.setText(men.getNombre());
    }//GEN-LAST:event_jButMaMeActionPerformed

