    private void jButEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButEliminarActionPerformed
        // TODO add your handling code here:

      if(entrada.Empty()){
          JOptionPane.showMessageDialog(this, "La pila esta llena...");
      }else{
          
     
        //Ponemos en 0 los renglones de la pila, para que no valla a 
        //agregar datos sobre los que ya habian.
        modelo.setRowCount(0);
        
        //Eliminamos el último registro.
        entrada.Pop();
       
       while(!entrada.Empty()){
            //Creamos un nuevo objeto, el cual tendra como parametros nuevamente
            //el objeto 'orden'.
            
            Object [] objeto = new Object[3];
            //El objeto orden, tendra la información de la pila entrada.pop() (El cual, siempre será el último).
            
            Elemento orden = entrada.Pop();
            auxis.Push(orden);
            //En la pila auxiliar, antes que todo, haremos un push del dato orden, en la pila aux.
            
            //El objeto, sera llenado a partir de los parametros que obtendremos de
            //el elemento orden.
            objeto[0] = orden.getEspecialidad();
            
             switch (orden.getTamaño()) {
                case 0:
                    objeto[1] = "Chica";
                    break;
                case 1:
                    objeto[1] = "Mediana";
                    break;
                case 2:
                    objeto[1] = "Grande";
                    break;
                default:
                    break;
            }
            
            objeto[2] = orden.getPrecio();
            
            //Cada que termine este llenado, vamos a ir agregando un nuevo renglon con 
            //la información del 'objeto'
            modelo.addRow(objeto);
            System.out.println(objeto);
            
        }   
       while(!auxis.Empty()){
            entrada.Push(auxis.Pop());
            jLabEstatus.setText("Eliminando.");
            jLabEstatus.setText("Eliminando..");
            jLabEstatus.setText("Eliminando...");
        }   jLabEstatus.setText("Eliminado! ");
      }
           
    }//GEN-LAST:event_jButEliminarActionPerformed

