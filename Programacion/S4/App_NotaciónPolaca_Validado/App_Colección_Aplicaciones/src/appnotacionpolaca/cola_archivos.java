    private void jButLlenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButLlenarActionPerformed
        // TODO add your handling code here:
        int col = modelo.getColumnCount();
          if (jRadioButton1.isSelected()) {
               for (int i = 0; i < col; i++) {
                modelo.setValueAt(az.nextInt(modelo.getColumnCount()), 0, i);
            }
               
        } else if (jRadioButton2.isSelected()) {
              for (int i = 0; i < col; i++) {
                modelo.setValueAt(nombres[az.nextInt(nombres.length)], 0, i);
            }
             
        } else if (jRadioButton3.isSelected()) {
              for (int i = 0; i < col; i++) {
               modelo.setValueAt((az.nextDouble()+az.nextInt(modelo.getColumnCount())), 0, i);
            }
        } else {
              for (int i = 0; i < col; i++) {
                modelo.setValueAt(nombres[az.nextInt(nombres.length)], 0, i);
            }
        }
        
    }//GEN-LAST:event_jButLlenarActionPerformed


