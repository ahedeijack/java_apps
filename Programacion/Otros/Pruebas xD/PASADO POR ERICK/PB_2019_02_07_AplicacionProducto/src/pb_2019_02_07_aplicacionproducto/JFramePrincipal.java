    private void jButCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCalcularActionPerformed
        // TODO add your handling code here:
        Producto x = new Producto();
        DecimalFormat f = new DecimalFormat("###,##0.00");
        
        x.setDescripcion(jTextDescripcion.getText());
        x.setCosto(Double.parseDouble(jTextCosto.getText()));
        x.setMargenUtilidad(Double.parseDouble(jTextMargen.getText()));
        x.setPorcentajeDescuento(Double.parseDouble(jTextDescuento.getText()));
        x.setPorcentajeImpuesto(Double.parseDouble(jTextImpuesto.getText()));
        
        jLabMargen.setText(f.format(x.importeMargenUtilidad()));
        jLabSubTotal1.setText(f.format(x.subTotal1()));
        jLabDescuento.setText(f.format(x.importeDescuento()));
        jLabSubTotal2.setText(f.format(x.subTotal2()));
        jLabImpuesto.setText(f.format(x.importeImpuesto()));
        jLabPrecio.setText(f.format(x.precio()));
    }//GEN-LAST:event_jButCalcularActionPerformed

