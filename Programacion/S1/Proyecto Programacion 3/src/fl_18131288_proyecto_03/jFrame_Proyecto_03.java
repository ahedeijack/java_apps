    private void jButtCalculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtCalculoActionPerformed
        //Se definen las variables que se utilizaran. 
        if(jRadB.isSelected()){
            //
        double n, calculo, s, valCalc;
        int i;
        i = 0;
        calculo = 0;
        s = -1;
        n = Double.parseDouble(jTextN.getText());
        
            //
        for (i = 0; i <= n ; i++ ){
            //Se calcula el valor, cuando la variable cSigno, es positiva se hace
            //La resta al valor1 inicial, sumando la repetición.
            if (s == 1){
                calculo = calculo - 1.0/(2*i+1);
		s = -1;
                System.out.println(""+calculo);
            } else {
                //De igual forma, se hace la misma validación para hacer la resta.
                calculo = calculo + 1.0/(2*i+1);
		s = 1;
                System.out.println(""+calculo);
            }
          }
        //Se multiplica el resultado por el valor de 4.
        //Recordemos que la formula original es PI/4 = (1-1/3+1/5-1/7...)
        //Despejando, el valor es PI = 4(1-1/3+1/5-1/7...)
        valCalc = (calculo * 4);
        jLabValUs.setText("Calculado con "+n+" terminos");
        jLabRes.setText(valCalc+"");        
        } 
           //Fin del primer if, cuando el RadButt esta seleccionado.
           //Si el boton esta sin seleccionar, se calculara el valor de PI
           //Que el usuario nos de. 
else {         
           //Aquí se hace el calculo, cuando el usuario da el valor de PI.
           //Se velve a utilizar el valor de N como PI.
       int i, terminos;
       double n,calculo, s;
       n = Double.parseDouble(jTextN.getText());
       if (n >= 4){
           jLabError.setText("Error! ");
       } else {
       s = 1;
       calculo = 0;
       i =0 ;
       terminos = 0;
  
           while (i!=-1){
           //Inicio de Ciclo
            if (s == 1){
                calculo = calculo + (double) 1/(2*i+1);
		s = -1;
            } else {
                //De igual forma, se hace la misma validación para hacer la resta.
                calculo = calculo - (double) 1/(2*i+1);
		s = 1;
            }
           if (n/(calculo * 4)>1){
               i = i+1;
           } else {
                    if (n/(calculo*4)>0.99999){
                        i = -1;
                    }
                    else{
                        i = i + 1;
                    }
           }
           terminos = terminos + 1;
           }
           //Fin de CICLO  
         
     jLabValUs.setText("Valor de "+n+", se calcula con:");    
     jLabRes.setText(terminos+" terminos.");     
            
    }
    }//GEN-LAST:event_jButtCalculoActionPerformed
    }
    
