    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        // TODO add your handling code here:
        
        int limI = 0; 
        int limS = 0; 
        
        try{
        limI = Integer.parseInt(jText1.getText()+"");
        limS = Integer.parseInt(""+jText2.getText());
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Tiene que existir un nÃºmero. Por defecto sera 0 y 10.");
            limI = 0;
            limS = 10;
        }
        v1.setText("Primos de "+limS+" a "+limI);
        
        
        int aux;
        if(limI >= limS){
          aux = limI;
          limI = limS;
          limS = aux;
        }
        
        modeloLista = new DefaultListModel();
        
        //Obtenemos el tamaÃ±o del arreglo.
        int c = 0;
        for(int i = limI; i <= limS; i++){
            if(Matematicas.Primos(i)){
             c++;   
            }
        }
        
        
        int arreglo [] = new int[c];
        int dato = 0;
        for(int x = limI; x<=limS; x++){
           if(Matematicas.Primos(x)){
               arreglo[dato++] = x;
               
           }
       }
        
        for(int x = 0; x<arreglo.length; x++){
         modeloLista.addElement(arreglo[x]);  
       }
    
    jList.setModel(modeloLista);

    }//GEN-LAST:event_jButton12ActionPerformed

