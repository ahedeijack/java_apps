    //funcion para sacar promedio.
    public double promedio(double total, JTable tabla){
       double [] [] ventas = new double [tabla.getRowCount()][tabla.getColumnCount()];
        
        double ventasTotales = 0;
        for(int r = 0; r<ventas.length; r++){
            for(int c = 0; c<ventas[0].length; c++){
                
                ventasTotales = (double) modelo.getValueAt(r, c);
        }   
        }
        
        double promedio;
        promedio = ventasTotales / (tabla.getRowCount()+tabla.getColumnCount());
        return promedio;
        
        
    }

