    private void jMenuGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuGuardarActionPerformed
        // TODO add your handling code here:
        jfch.showSaveDialog(jMenu1);
        File archivo = jfch.getSelectedFile();
        
        if (archivo == null)
            return;
        
        FileWriter fw = null;
        PrintWriter pw = null;
        
        try
        {
            fw = new FileWriter(archivo);
            pw = new PrintWriter(fw);

            pw.println(parEncontrados);
            pw.println(intentos);
            
            for (int i=0; i<pares.length; i++)
            {
                pw.println(pares[i].getSigno());
                pw.println(pares[i].isEnabled());
                //pw.println(pares[i].getBackground().getRGB());
            }
        }
        catch(IOException ex)
        {
            Logger.getLogger(PB_17131615_Frame_04.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally
        {
            if (pw!=null)
                pw.close();
            if (fw!=null)
                try {
                    fw.close();
            } catch (IOException ex) {
                Logger.getLogger(PB_17131615_Frame_04.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jMenuGuardarActionPerformed

    private void jMenuAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuAbrirActionPerformed
        // TODO add your handling code here:
        jfch.showOpenDialog(jMenu1);
        File archivo = jfch.getSelectedFile();
        
        if (archivo == null)
            return;
        
        FileReader fr = null;
        BufferedReader br = null;
        
        try
        {
            fr = new FileReader(archivo);
            br = new BufferedReader(fr);

            parEncontrados = Integer.parseInt(br.readLine());
            intentos = Integer.parseInt(br.readLine());
            
            jLabPares.setText(parEncontrados+"");
            jLabIntentos.setText(intentos+"");
            
            for (int i=0; i<pares.length; i++)
            {
                pares[i].setSigno(br.readLine());
                
                if (br.readLine().equals("true"))
                {
                    pares[i].setText("");
                    pares[i].setEnabled(true);
                }
                else
                {
                    pares[i].mostrarSigno();
                    pares[i].setEnabled(false);
                }
                
            }
        }
        catch (FileNotFoundException ex)
        {
            Logger.getLogger(PB_17131615_Frame_04.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (IOException ex)
        {
            Logger.getLogger(PB_17131615_Frame_04.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally
        {
            try {
                if (br != null)
                    br.close();
                if (fr != null)
                    fr.close();
            } catch (IOException ex) {
                Logger.getLogger(PB_17131615_Frame_04.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jMenuAbrirActionPerformed

