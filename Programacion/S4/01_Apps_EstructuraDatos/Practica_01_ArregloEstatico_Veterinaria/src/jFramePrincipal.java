    private void jButVisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButVisualizarActionPerformed
        // TODO add your handling code here:
        Animales [ ] grupoAux = getGrupoSel();
        int nGpoSelec = jCbGrupo.getSelectedIndex();
        if(tamañoMaximoGpo[nGpoSelec] < 5){
        int vacunas = Integer.parseInt(jSpinVacunas.getValue()+"");
        grupoAux[tamañoMaximoGpo[nGpoSelec]++].Leer(jTextNombre.getText(), jCBTipo.getSelectedItem().toString(), vacunas,
                Double.parseDouble(jTextPrecioTotal.getText()+""), 
                jRadioButton1.isSelected()?'H':'M',
                Integer.parseInt(jSpinEdad.getValue().toString()));
        }
        else{
          JOptionPane.showMessageDialog(this,
                "ERROR ... YA NO HAY CUPO EN EL GRUPO SELECCIONADO", 
                "GRUPO LLENO",JOptionPane.PLAIN_MESSAGE); 
        }
    
    }//GEN-LAST:event_jButVisualizarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
Animales [ ] grupoAux = getGrupoSel();
        int nGpoSelec = jComboGrupo.getSelectedIndex();
        if(nMasxGpo[nGpoSelec] < 5){
        grupoAux[nMasxGpo[nGpoSelec]++].Leer(this.jTextNombre.getText(), 
                this.jComboBoxTipo.getSelectedItem().toString(),
                Integer.parseInt(this.jTextEdad.getText()), //Edad
                Integer.parseInt(this.jTextNumVac.getText()), //Num Vacunas
                Double.parseDouble(this.jTextCostTotalVac.getText()),
                this.jRadioBMacho.isSelected()?"Macho":"Hembra");
        jTableEspaciosGrupos.setValueAt(nMasxGpo[nGpoSelec], 0, nGpoSelec);
        }
        else{
          JOptionPane.showMessageDialog(this,
                "ERROR ... YA NO HAY CUPO EN EL GRUPO SELECCIONADO", 
                "GRUPO LLENO",JOptionPane.PLAIN_MESSAGE); 
        }
        jButtonVisualizar.doClick();    
        

    }//GEN-LAST:event_jButton1ActionPerformed

