    private void jButCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCalcularActionPerformed
        // TODO add your handling code here:
        Contenido inicial = new Contenido();
        Contenido terminal = new Contenido();
        Contenido consumo;
        Contenido totalInicial = new Contenido();
        Contenido totalTerminal = new Contenido();
        Contenido totalConsumo = new Contenido();
        
        for (int r=0; r<jTableFabrica.getRowCount(); r++)
        {
            inicial.setBarriles(Integer.parseInt(jTableFabrica.getValueAt(r, 1)+""));
            inicial.setGalones(Integer.parseInt(jTableFabrica.getValueAt(r, 2)+""));
            
            
            totalInicial = totalInicial.suma(inicial);
            
            terminal.setBarriles(Integer.parseInt(jTableFabrica.getValueAt(r, 3)+""));
            terminal.setGalones(Integer.parseInt(jTableFabrica.getValueAt(r, 4)+""));
            
            totalTerminal = totalTerminal.suma(terminal);
            
            consumo = inicial.resta(terminal);
            
            jTableFabrica.setValueAt(consumo.getBarriles(), r, 5);
            jTableFabrica.setValueAt(consumo.getGalones(), r, 6);
            
            totalConsumo = totalConsumo.suma(consumo);
           
        }
        jTableTotales.setValueAt(totalInicial.toString(), 0, 0);
        jTableTotales.setValueAt(totalTerminal.toString(), 0, 1);
        jTableTotales.setValueAt(totalConsumo.toString(), 0, 2);
    }//GEN-LAST:event_jButCalcularActionPerformed

