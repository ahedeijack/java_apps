    private void jButGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButGuardarActionPerformed
        // TODO add your handling code here:
        
        int posicion = (int)jSpinPos.getValue()-1;
        
        Empleado x = new Empleado();
        
        x.setNombre(jTextNombre.getText());
        x.setDiasTrabajados(Integer.parseInt(jTextDias.getText()));
        x.setSueldoDiario(Double.parseDouble(jTextSueldo.getText()));
        x.setHorasExtra(Integer.parseInt(jTextHoras.getText()));
        x.setPorcentajeImp(Double.parseDouble(jTextImpuestos.getText()));
        
        DecimalFormat p = new DecimalFormat("#.000");
        nomina[posicion] = x;
        
        for (int r = 0; r < nomina.length; r++){
            jTabNomina.setValueAt(nomina[r].getNombre(), r, 0);
            jTabNomina.setValueAt(nomina[r].getDiasTrabajados(), r, 1);
            jTabNomina.setValueAt("$"+p.format(nomina[r].getSueldoDiario()), r, 2);
            jTabNomina.setValueAt(nomina[r].getHorasExtra(), r, 3);
            jTabNomina.setValueAt(nomina[r].getPorcentajeImpuesto(), r, 4);
            
            jTabNomina.setValueAt("$"+p.format(nomina[r].importeHorasExtra()), r, 5);
            jTabNomina.setValueAt("$"+p.format(nomina[r].importeImpuesto()), r, 6);
            jTabNomina.setValueAt("$"+p.format(nomina[r].sueldo()), r, 7);
        }
        
        if (jCheckBox1.isSelected()){
           if (posicion == nomina.length -1){
               jSpinPos.setValue(1);
           } else {
               jSpinPos.setValue(posicion + 2);
           }
        } 
            
        
    }//GEN-LAST:event_jButGuardarActionPerformed

