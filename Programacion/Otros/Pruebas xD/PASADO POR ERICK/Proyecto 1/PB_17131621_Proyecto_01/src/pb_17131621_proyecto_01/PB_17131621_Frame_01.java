    //Codigo Boton Grabar
    private void jButGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButGrabarActionPerformed
        // TODO add your handling code here:
        int posicion = (int)jSpinPosicion.getValue()-1;
        
        Rectangulo rec = new Rectangulo(jTextNombre.getText(),
                                        Double.parseDouble(jTextBase.getText()),
                                        Double.parseDouble(jTextPerimetro.getText()));
        
        rectangulo.setRectangulo(rec, posicion);
        
        rectangulo.mostrar(jTableRectangulo, jLabTotalArea, jLabPromedioAltura);  
        
        if (jCheckBox1.isSelected())
            if((int)jSpinPosicion.getValue()<3)
            jSpinPosicion.setValue(posicion+2);
    }//GEN-LAST:event_jButGrabarActionPerformed
    
