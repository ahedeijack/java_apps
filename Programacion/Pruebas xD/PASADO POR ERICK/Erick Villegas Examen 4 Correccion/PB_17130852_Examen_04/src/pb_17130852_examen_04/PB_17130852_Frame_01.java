    private void jMenuItemGBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemGBActionPerformed
        // TODO add your handling code here:
        jfch.showSaveDialog(jMenu1);
        
        File archivo = jfch.getSelectedFile();
        
        if (archivo == null) // seleccionó Cancelar
            return;
        
        FileOutputStream fos = null;
        DataOutputStream dos = null;
        
        try 
        {
            fos = new FileOutputStream(archivo);
            dos = new DataOutputStream(fos);
            
            dos.writeInt((int)jSpinAutos.getValue());
            
            for (int r=0; r<(int)jSpinAutos.getValue(); r++)
            {
                dos.writeUTF(jTableAutos.getValueAt(r, 0)+"");
                dos.writeUTF(jTableAutos.getValueAt(r, 1)+"");
                dos.writeUTF(jTableAutos.getValueAt(r, 2)+"");
            }
        } 
        catch (FileNotFoundException ex) 
        {
            Logger.getLogger(PB_17130852_Frame_01.class.getName()).log(Level.SEVERE, null, ex);
        } 
        catch (IOException ex) 
        {
            Logger.getLogger(PB_17130852_Frame_01.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally
        {
            try {
                if (dos!=null)
                    dos.close();
                if (fos!=null)
                    fos.close();
            } 
            catch (IOException ex) 
            {
                Logger.getLogger(PB_17130852_Frame_01.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jMenuItemGBActionPerformed

