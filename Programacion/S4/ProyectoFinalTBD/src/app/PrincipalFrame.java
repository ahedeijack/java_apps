    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        int pos = jTablePrincipal.getSelectedRow();
        if (pos == -1) {
            JOptionPane.showMessageDialog(this, "No hay un registro seleccionado", 
                    "Eliminar", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String id =(String) this.jTablePrincipal.getValueAt(this.jTablePrincipal.getSelectedRow(),0); 
        switch (moduloActual) {
            case TIT_MODULO1:
                try {
                    objcon.delJefe(id);
                    objcon.mosrarDatosEmpleado(jTablePrincipal);
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, ex, "Error", JOptionPane.ERROR_MESSAGE);
                }
                break;
            case TIT_MODULO3:
                try {
                    objcon.delEmpleado(id);
                    objcon.mosrarDatosEmpleado(jTablePrincipal);
                } catch (SQLException ex) {
                    Logger.getLogger(PrincipalFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;
                
            case TIT_MODULO4:
                try {
                    objcon.delCliente(id);
                    objcon.mosrarDatosCliente(jTablePrincipal);
                } catch (SQLException ex) {
                    Logger.getLogger(PrincipalFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;
                
            case TIT_MODULO5:
                try {
                    objcon.delPasillo(id);
                    objcon.mosrarDatosPasillo(jTablePrincipal);
                } catch (SQLException ex) {
                    Logger.getLogger(PrincipalFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
               break;
        }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

