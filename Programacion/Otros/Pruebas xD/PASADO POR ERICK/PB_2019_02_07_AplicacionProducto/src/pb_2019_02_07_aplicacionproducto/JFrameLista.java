    private void jButAleatoriosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButAleatoriosActionPerformed
        // TODO add your handling code here:
        String [] prod = {"PlayStation4", "PC Master Race", "Nintendo Switch", "Samsung Galaxy A8", "XboxOne",
                          "GTX 1080 Ti", "Spec Omega", "Boleto a Sonora"};
        
        double [] imp = {0, 6, 10, 12, 16, 20, 24.5, 44};
        
        Random azar = new Random();
        
        for (int r=0; r<jTableLista.getRowCount(); r++)
        {
            jTableLista.setValueAt(prod[azar.nextInt(prod.length)], r, 0);
            jTableLista.setValueAt(100+azar.nextInt(20000), r, 1);
            jTableLista.setValueAt(5+azar.nextInt(26), r, 2);
            jTableLista.setValueAt(azar.nextInt(25), r, 3);
            jTableLista.setValueAt(imp[azar.nextInt(imp.length)], r, 4);
        }
    }//GEN-LAST:event_jButAleatoriosActionPerformed

    private void jButCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCalcularActionPerformed
        // TODO add your handling code here:
        Producto x = new Producto();
        DecimalFormat d = new DecimalFormat("###,##0.00");
        
        for (int r=0; r <jTableLista.getRowCount(); r++) 
        {
            x.setCosto(Double.parseDouble(jTableLista.getValueAt(r, 1)+""));
            x.setMargenUtilidad(Double.parseDouble(jTableLista.getValueAt(r, 3)+""));
            x.setPorcentajeDescuento(Double.parseDouble(jTableLista.getValueAt(r, 3)+""));
            x.setPorcentajeImpuesto(Double.parseDouble(jTableLista.getValueAt(r, 4)+""));
           
            
            jTableLista.setValueAt(x.importeMargenUtilidad(), r, 5);
            jTableLista.setValueAt(x.importeDescuento(), r, 6);
            jTableLista.setValueAt(x.importeImpuesto(), r, 7);
            jTableLista.setValueAt(x.precio(), r, 8);
        }
    }//GEN-LAST:event_jButCalcularActionPerformed

