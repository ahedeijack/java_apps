    private void jButCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCalcActionPerformed
        // TODO add your handling code here:
        
        DecimalFormat f = new DecimalFormat("###.00"); //Sirve para determinar los decimales
        String nombre;
        int asis, tar, proy, ex;
        
        nombre =  jTextAl1.getText();
        asis = Integer.parseInt(jTextAsis1.getText());
        tar = Integer.parseInt(jTextTarea1.getText());
        proy = Integer.parseInt(jTextProy1.getText());
        ex = Integer.parseInt(jTextEx1.getText());
        
        alm uno = new alm();
        
        uno.setNom(nombre);
        uno.setAsis(asis);
        uno.setTar(tar);
        uno.setProye(proy);
        uno.setExa(ex);
        jLabCal1.setText(f.format(uno.cali())); //F hace que sean menos decimales
        
        alm dos = new alm(jTextAl2.getText(),
        Integer.parseInt(jTextAsis2.getText()),
        Integer.parseInt(jTextTarea2.getText()),
        Integer.parseInt(jTextProy2.getText()),
        Integer.parseInt(jTextEx2.getText()));
        
        jLabCal2.setText(f.format(dos.cali()));
        
        alm tres = new alm(uno);
        jLabCal3.setText(f.format(tres.cali()));
        
        tres.setNom(jTextAl3.getText());
        tres.setAsis(Integer.parseInt(jTextAsis3.getText()));
        tres.setTar(Integer.parseInt(jTextTarea3.getText()));
        tres.setProye(Integer.parseInt(jTextProy3.getText()));
        tres.setExa(Integer.parseInt(jTextEx3.getText()));
        
        
        double promAsis, promTar, promProy, promEx;
        promAsis = (uno.getAsist() + dos.getAsist() + tres.getAsist())/3.0;
        promTar  = (uno.getTareas() + dos.getTareas() + tres.getTareas())/3.0;
        promProy = (uno.getProy() + dos.getProy() + tres.getProy())/3.0;
        promEx   = (uno.getExa() + dos.getExa() + tres.getExa())/3.0;
        
        double promGral = (uno.cali() + dos.cali() + tres.cali())/ 3.0;
        
        jPromAsis.setText(f.format(promAsis));
        jPromTar.setText(f.format(promTar));
        jPromPro.setText(f.format(promProy));
        jPromEx.setText(f.format(promEx));
        jLabCalF.setText(f.format(promGral));
    }//GEN-LAST:event_jButCalcActionPerformed

