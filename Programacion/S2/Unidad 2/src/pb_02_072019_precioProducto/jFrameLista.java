    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //Creamos un arreglo de referencias en el mismo numero de elementos que el de renglones de Jtable
        Producto [] lista = new Producto[jTabLista.getRowCount()];
            
            //Creamos los objetos y les asignamos los valores del jTable
            for (int r = 0; r<lista.length; r++){
                lista[r] = new Producto         (
                                                    jTabLista.getValueAt(r, 0)+"", //Descripcion
                                                    Double.parseDouble(jTabLista.getValueAt(r, 1)+""),//Costo
                                                    Double.parseDouble(jTabLista.getValueAt(r, 2)+""),//%Margen
                                                    Double.parseDouble(jTabLista.getValueAt(r, 3)+""),//%Desc
                                                    Double.parseDouble(jTabLista.getValueAt(r, 4)+"")//%Impuesto
                                                 );
            }
            //Ordenamosel arreglo
            //No se pueden ordenar objetos con la sig. funcion: Arrays.sort(lista);
            //Algoritmo para ordenar de manera ascendente el arreglo de -datos- u -objetos- de 'n' datos
            //Bubble Sort - Método de la burbuja
            //Desde i <- 1 HASTA n-1
            //Desde j<- i+1 HASTA n
            //SI datos[i]>datos[j]
            //aux <- datos[i]
            //datos[i]<-[j]
            //datos[j]<-aux
            //Fin_SI
            //Fin_desde
            //Fin_desde
            
            for(int i = 0; i<(lista.length-1); i++){
                for (int j = i+1; j<lista.length; j++ ){
                    if (lista[i].Precio() > lista[j].Precio()){ //Se pide el atributo a comparar ".Precio()"
                        Producto aux = lista[i];
                        lista[i] = lista[j];
                        lista[j] = aux;
                    }
                }
            }
            
            for (int r = 0; r<lista.length; r++){
                jTabLista.setValueAt(lista[r].getDescpcion(), r, 0);
                jTabLista.setValueAt(lista[r].getCosto(), r, 1);
                jTabLista.setValueAt(lista[r].getMargUt(), r, 2);
                jTabLista.setValueAt(lista[r].getPorDesc(), r, 3);
                jTabLista.setValueAt(lista[r].getPorImp(), r, 4);
                
                jTabLista.setValueAt(lista[r].marUt(), r, 5);
                jTabLista.setValueAt(lista[r].importeDescc(), r, 6);
                jTabLista.setValueAt(lista[r].subTotal1(), r, 7);
                jTabLista.setValueAt(lista[r].subTotal2(), r, 8);
            }
    }//GEN-LAST:event_jButton2ActionPerformed

