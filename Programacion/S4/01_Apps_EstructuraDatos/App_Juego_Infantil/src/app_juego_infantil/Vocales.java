    private void jAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAActionPerformed
        // TODO add your handling code here:
        StringBuilder info = new StringBuilder(jLabIm.getIcon().toString());
        char voc = info.charAt(info.length()-5);
        if (voc == 'a') {
            JOptionPane.showMessageDialog(this, "Excelente! Acercaste! El nombre del animal empieza con A");
            jLabIm.setIcon(new ImageIcon(getClass().getResource(imgs[rand.nextInt(imgs.length)])));
        } else {
            JOptionPane.showMessageDialog(this, "Nopi, el nombre del animal no empieza con a unu");
        }
    }//GEN-LAST:event_jAActionPerformed

