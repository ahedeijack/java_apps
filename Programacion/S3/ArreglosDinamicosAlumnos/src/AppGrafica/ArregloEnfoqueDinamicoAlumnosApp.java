    private void eliReprobados(ArrayList<Alumno> grupo, int col){
        for(int i=0; i<grupo.size(); i++){
            if(grupo.get(i).getCalif()<70){
                grupo.remove(i);
            --i;
        } //Importante cerrar el IF
        }
    tblNoAlusGpo.setValueAt(grupo.size(), 0, col);
    }
    
