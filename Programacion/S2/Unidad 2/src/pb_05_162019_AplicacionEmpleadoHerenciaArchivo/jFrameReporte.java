    private void jButPorSueldoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButPorSueldoActionPerformed
        // TODO add your handling code here:
        for(int i=0; i<copia.length-1; i++)
            for(int j=i+1; j<copia.length; j++)
                if(copia[i].sueldo() < copia[j].sueldo())
                {    
                    Empleado aux = copia[i];
                    copia[i] = copia[j];
                    copia[j] = aux;
                }
        mostrar();
    }//GEN-LAST:event_jButPorSueldoActionPerformed

    private void jButPorAlfabeticoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButPorAlfabeticoActionPerformed
        // TODO add your handling code here:
        for(int i=0; i<copia.length-1; i++)
            for(int j=i+1; j<copia.length; j++)
                if(copia[i].getNombre().compareTo(copia[j].getNombre())>0)
                {    
                    Empleado aux = copia[i];
                    copia[i] = copia[j];
                    copia[j] = aux;
                }
        mostrar();    
    }//GEN-LAST:event_jButPorAlfabeticoActionPerformed

