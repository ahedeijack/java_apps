    public void mostrar(DefaultTableModel tabla, Pila entrada){
        
        Pila auxi = new Pila(entrada.Length());
        tabla.setRowCount(0);
        
        while(!entrada.Empty()){
            Zapatos dato = entrada.Pop();
            auxi.Push(dato);
            
            Object o [] = new Object[3];
            o[0] = dato.getMarca();
            o[1] = dato.getMedida();
            o[2] = dato.getPrecio();
            tabla.addRow(o);
        } while (!auxi.Empty()){
            entrada.Push(auxi.Pop());
        }
        
        
    }
