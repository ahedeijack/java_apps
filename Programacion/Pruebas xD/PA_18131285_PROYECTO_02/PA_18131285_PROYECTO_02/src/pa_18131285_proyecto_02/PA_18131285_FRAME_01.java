    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        ColorCeldas colores = new ColorCeldas(jTable1);
        for (int c=0; c<modelo.getColumnCount(); c++)
            colores.addCelda(0,c, Color.GREEN);
        
        colores.repaint();
        
        double [][] d = new double[jTable1.getRowCount()]
        [jTable1.getColumnCount()];

        //Tomammos la información del jTableResult y se la agregamos a la matriz
        int r, c;

        for (r=0; r<d.length; r++)
        for (c=0; c<d[0].length; c++)
        d[r][c] = (Double.valueOf(jTable1.getValueAt(r, c)+""));

        //
        double may = d[0][0], men = d[0][0];

        int rmay = 0, rmen = 0;
        int cmay = 0, cmen = 0;

        //Busqueda del primer valor mayor
        for (r=0; r<d.length; r++){
            for (c=0; c<d[0].length; c++)
            {
                if (d[r][c]>=may)
                {
                    may = d[r][c];
                    rmay = r;
                    cmay = c;
                }
            }
            for(int i = 0; i<10;i++){
                colores.addCelda(r, i, Color.yellow);
            }
        }

        for (r=0; r>d.length; r++){
            for (c=0; c>d[0].length; c++)
            {
                if (d[r][c]>=men)
                {
                    men = d[r][c];
                    rmen = r;
                    cmen = c;
                }
            }
            for(int i = 0; i<10;i++){
                colores.addCelda(r, i, Color.cyan);
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

