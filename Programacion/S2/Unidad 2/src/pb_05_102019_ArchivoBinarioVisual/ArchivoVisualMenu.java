    private void jMenuNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuNuevoActionPerformed
        // TODO add your handling code here:
        
        if(jfch.showSaveDialog(jLabel1) != 0){
            return;
        }
        FileOutputStream fos = null;
        DataOutputStream dos = null;
        
         try {
            // TODO add your handling code here:

             fos = new FileOutputStream(jfch.getSelectedFile());
             dos = new DataOutputStream(fos);
            
            dos.writeInt(modelo.getRowCount());
            
            for(int r=0; r<modelo.getRowCount(); r++){
                dos.writeUTF((String)modelo.getValueAt(r, 0));
                for(int c = 1; c<modelo.getColumnCount()-1; c++){
                    dos.writeInt((int)modelo.getValueAt(r, c));
                }
            }
       
        } catch (IOException ex) {
            Logger.getLogger(ArchivoVisualMenu.class.getName()).log(Level.SEVERE, null, ex);
        }  finally {
             
            try {
                
                if (dos!= null) {
                    dos.close();
                }
                
                if(fos!= null){
                    fos.close();
                }
               
            } catch (IOException ex) {
                Logger.getLogger(ArchivoVisualMenu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
        
    }//GEN-LAST:event_jMenuNuevoActionPerformed

    private void jRadioButtonMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButtonMenuItem2ActionPerformed
        // TODO add your handling code here:
        
        FileReader fr = null;
        BufferedReader br = null;
        
        if(jfch.showOpenDialog(jLabel1) != 0){
            return;
        }
        
        
         try {
            // TODO add your handling code here:
            fr = new FileReader(jfch.getSelectedFile());
            br = new BufferedReader(fr);
            
            int n =  Integer.parseInt( br.readLine());
            
            jSpinAlumnos.setValue(n);
            
            for(int r = 0; r < n; r++){
                for(int c = 0; c<modelo.getColumnCount()-1; c++){
                    modelo.setValueAt(br.readLine(), r, c);
                }
            }
            jButCalcular.doClick();
            
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(rootPane, "Archivo no encontrado."+ex.getMessage());
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(rootPane, "Problemas con el archivo."+ex.getMessage());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error al leer\n(Archivo Dañado)\n."+ex.getMessage());
        } finally {
           
            try {
                
                 if(br!= null)
                 br.close();
             
             if(fr!= null)
                 fr.close();
             
            } catch (IOException ex) {
                Logger.getLogger(ArchivoVisualMenu.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
            
         }
        
        
        
    }//GEN-LAST:event_jRadioButtonMenuItem2ActionPerformed

