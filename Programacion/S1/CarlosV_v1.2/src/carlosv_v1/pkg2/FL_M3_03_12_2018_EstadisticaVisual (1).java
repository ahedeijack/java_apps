    public static double mediana (double [] x){
        double med;
        
        Arrays.sort(x);
        
        if (x.length % 2 == 0){
            //El número es par
            med = ((x[x.length/2-1]+x[x.length/2])/2);
        } else {
            //El número es non
            med = x[(x.length - 1)/2];
        }
        
               
    return med;
    }
