    public void llenar(){
        cuerpos = new Cuerpo[modelo.getRowCount()];
        energias = new Energia[modelo.getRowCount()];
        
        double total = 0;
        double promedio = 0;
        
        for (int i = 0; i < modelo.getRowCount(); i++) {
            
            energias[i] = new Energia(Double.parseDouble(modelo.getValueAt(i, 1)+""),
                                                       Double.parseDouble(modelo.getValueAt(i, 2)+""));
        
           cuerpos[i] = new Cuerpo(modelo.getValueAt(i, 0)+"",
                                                    energias[i]);
           
           modelo.setValueAt(cuerpos[i].getEnergia().obtenerMasa(), i, 3);
           
           total += Double.parseDouble(modelo.getValueAt(i, 3)+"");
           promedio += Double.parseDouble(modelo.getValueAt(i, 2)+"");
        }
        promedio = promedio / modelo.getRowCount();
        jLabMasaTotal.setText(total+"");
        jLabPromedioVelocidad.setText(promedio+"");   
    }

