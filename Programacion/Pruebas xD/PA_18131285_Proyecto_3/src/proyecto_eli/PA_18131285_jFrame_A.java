    dibujos[2] = c3;
    dibujos[3] = c4;
    
    for (int i = 0; i < citys.length; i++) {
        for (int j = 0; j < citys.length; j++) {
            if (citys[i].getColindaCon().compareTo(citys[j].getColindaCon())>0) {
                
                PA_18131285_Clase_A aux = citys[i];
                citys[i] = citys[j];
                citys[j] = aux;
                
                
            }
        }
    }
