    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        double [][] datos = new double [jTableAlumnos.getRowCount()][jTableAlumnos.getColumnCount()];
        
        int r, c;
        //Se copian los valores del jTable a la matriz. 
        
        for (r = 0; r<datos.length; r++){
            for (c = 0; c<datos[0].length; c++){
                datos[r][c] = Double.parseDouble(jTableAlumnos.getValueAt(r, c)+"");
            }
        }
        //Se calcula la suma, promedio, mayot y menor
        double suma, promedio, mayor, menor;
        int renMay =0, renMen=0, colMay =0, colMen= 0;
        
        
        suma = 0;
        mayor = datos [0][0];
        menor  = datos[0][0];
        
        for (r = 0; r<datos.length; r++){
            for (c = 0; c<datos[0].length; c++){
                suma += datos[r][c];
                
                if(datos[r][c]>mayor){
                    mayor = datos[r][c];
                    renMay = r;
                    colMay = c;
                }
                if(datos[r][c]<menor){
                    menor = datos[r][c];
                    renMen = r;
                    colMay = c;
                    
                }
                    
                    
                
                
            }
            promedio = suma / (datos.length * datos[0].length);
            jLabSum.setText(suma+"");
            jLabProm.setText(promedio+"");
            jLabMay.setText(mayor+"");
            jLabMen.setText(menor+"");
            ColorCeldas celdas = new ColorCeldas(jTableAlumnos);
            celdas.addCelda(renMay, colMay, Color.red);
            celdas.addCelda(renMen, colMen, Color.yellow);
            celdas.repaint();
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

