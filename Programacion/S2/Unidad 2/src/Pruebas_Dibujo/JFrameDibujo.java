    private void jButMoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButMoverActionPerformed
        // TODO add your handling code here:
        Component comp;
        
        if(jRBFrame.isSelected()){
            comp = this;
        } else {
            comp = this;
        }
        
        int ver = 0, hor= 0;
        
        for(int v = 1; v<35; v=v+5){
            for(int h = 1; h<35; h = h+5){
                ver += 2;
                hor +=1;
                dibujar(ver, hor*2, comp); //Se utiliza un multiplicador *2, para que el movimiento horizontal sea un poco mas ligero
            }
        }
        
    }//GEN-LAST:event_jButMoverActionPerformed
    public void dibujar(int x, int y, Component c){
        Graphics papel = c.getGraphics(); //this / c, hace referencia al frame actual 
        
        
        
        papel.setColor(red);
        papel.fillRect(5, 70, c.getWidth()-10, 135);
        
        papel.setColor(Color.blue);
                          //H          V       H   W
        papel.fillOval(500-x, y+70, 10,10);
        
        
        papel.drawRect(x+50, y+95, y+80, x+25);
        papel.drawLine(x+30, y+120, y+50, x+95);
        papel.drawLine(x+150, y+120, y+130, x+95);
        papel.drawRect(x+15, y+120, y+170, x+30);
        
        papel.setColor(Color.yellow);
        papel.fillRect(x+20, y+125, y+160, x+20);
        
        papel.setColor(Color.gray);
        papel.setFont(new Font("Centhury Gothic", Font.ITALIC,18));
        papel.drawString("tec laguna", x+50, y+140 );
        
        papel.setColor(Color.red);
        papel.fillOval(x+40, y+145, y+20, x+20);
        papel.fillOval(x+130, y+145, y+20, x+20);
        
        papel.setColor(Color.blue);
        papel.drawLine(x+10, y+167, x+190, y+167);
//        
        try {
            Thread.sleep(90);
        } catch (InterruptedException ex) {
            Logger.getLogger(JFrameDibujo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }
