    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        ex = new Experimentos[modelo.getRowCount()];
        for(int i = 0; i<ex.length; i++){
            ex[i]=new Experimentos(Double.parseDouble(modelo.getValueAt(i, 0)+""),
                                   Double.parseDouble(modelo.getValueAt(i, 1)+""));
        }
        for(int i = 0; i<ex.length; i++){
            modelo.setValueAt(ex[i].fuerza(), i, 2);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButOrdenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButOrdenActionPerformed
        // TODO add your handling code here:
        ex = new Experimentos[modelo.getRowCount()];
        for(int i = 0; i<ex.length; i++){
            ex[i]=new Experimentos(Double.parseDouble(modelo.getValueAt(i, 0)+""),
                                   Double.parseDouble(modelo.getValueAt(i, 1)+""));
        }
        for(int i = 0; i<ex.length; i++){
            modelo.setValueAt(ex[i].fuerza(), i, 2);
        }
        
        
        for(int i = 0; i<modelo.getRowCount(); i++){
            for(int j = 0; j<modelo.getRowCount(); j++){
            if(ex[i].fuerza()>ex[j].fuerza()){
             Experimentos aux = ex[i];
             ex[i] = ex[j];
             ex[j]=aux;
             
            }
           }
        }
        mostrar();
        
    }//GEN-LAST:event_jButOrdenActionPerformed

