    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
//Creamos las variables que van a llenar nuestro objeto del tipo Zapatos.
        marca = jSMar.getValue()+"";
        tamaño = Double.parseDouble(jSMed.getValue()+"");

//Marcas: Adidas,Jordan,Sketchers,Puma,Supra,Nike

        switch(marca){
                    case "Adidas": precio = 1399.00;
                    break;
                    case "Jordan": precio = 1119.00;
                    break;
                    case "Sketchers": precio = 1200.00;
                    break;
                    case "Puma": precio =  999.00;
                    break;
                    case "Supra": precio = 1799.00;
                    break;
                    case "Nike": precio = 1499.00;
                    break;
                    default: precio = 750.00;
                    break;
                }
        
        Zapatos caja = new Zapatos(marca, tamaño, precio);
        jLPrec.setText("$$"+precio);
        
        //Una vez creado el objeto, lo metemos en nuestra pila, posteriormente lo que haremos sera 
        //mandar a llamar el metodo de mostrar, con la pila y la tabla correspondiente.
        
        entrada.Push(caja);
        mostrar(tienda, entrada);
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(!entrada.Empty()){
            Zapatos caja = entrada.Pop();
            salida.Push(caja);
            mostrar(tienda, entrada);
            mostrar(inventario, salida);
            
                } else if(entrada.Empty()){
                        JOptionPane.showMessageDialog(this, "El inventario esta vacío...");
                        
                                } else if (salida.Full()) {
                                        JOptionPane.showMessageDialog(this, "La tienda esta llena...");
                                            }
        
    }//GEN-LAST:event_jButton2ActionPerformed

