    private void jSpinCircuitosStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinCircuitosStateChanged
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel)jTabPrincipal.getModel();
        int posicionC = (int)jSpinCircuitos.getValue();
        
        if (posicionC > jTabPrincipal.getRowCount()-1) 
        {
            modelo.addRow(new Object[]{0,0,0,0,0});
            principal = new Circuito((int)jSpinCircuitos.getValue());
            modelo.setRowCount(principal.getTamano()); 
        }
        else if(posicionC < jTabPrincipal.getRowCount())
        {
            modelo.removeRow(jTabPrincipal.getRowCount()-2);
            principal = new Circuito((int)jSpinCircuitos.getValue());
            modelo.setRowCount(principal.getTamano()); 
        }
        principal.mostrar(jTabPrincipal, jLabPromedio);
        //ASDASDAS
    }//GEN-LAST:event_jSpinCircuitosStateChanged

