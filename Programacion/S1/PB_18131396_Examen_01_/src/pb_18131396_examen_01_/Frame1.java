    public void mostrar()
        {
            double tot = 0;
            double prom = 0;
            for (int r=0; r<auto.length; r++)
            {
                jTable1.setValueAt(auto[r].getModelo(),r,0);
                jTable1.setValueAt(auto[r].getCosto(),r,1);
                jTable1.setValueAt(auto[r].getPrecio(),r,2);
                jTable1.setValueAt(auto[r].Utilidad(),r,3);
                
                tot += auto[r].Utilidad();
                prom = tot/auto.length;
            }
            jProm.setText(prom+"");
        }

