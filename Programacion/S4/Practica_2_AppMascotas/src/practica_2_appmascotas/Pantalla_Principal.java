    private void eliCostVacuMay100(Mascota [] grupo,int col) {        
         if (nMasxGpo[col] > 0) {
            Mascota[] aux = new Mascota[nMasxGpo[col]];
            for(int i=0;i<nMasxGpo[col];i++)
                aux[i] = new Mascota();
            int nCost = 0;
            for(int i=0;i<nMasxGpo[col];i++) 
                if (grupo[i].getCostVacunas() < 100)
                    aux[nCost++].Copia(grupo[i]);
            for(int i=0;i < nCost;i++) 
                grupo[i].Copia(aux[i]);
            nMasxGpo[col] = nCost;
        
        this.jTableEspaciosGrupos.setValueAt(nMasxGpo[col], 0, col);
    }
    }
    
